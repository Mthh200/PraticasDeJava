public class Cliente{
    String nome;
    int idade;
    int rendaAnual;


    

    public String getNome(){
        return this.nome;
    }
    public void setNome(String nome){
        this.nome = nome;
    }

    public int getIdade(){
        return this.idade;
    }
    public void setIdade(int idade){
        this.idade = idade;
    }

    public int getRendaAnual() {
        return rendaAnual;
    }
    public void setRendaAnual(int rendaAnual) {
        this.rendaAnual = rendaAnual;
    }
    
}